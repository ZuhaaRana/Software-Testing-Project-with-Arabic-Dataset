package to;

public class HadithTO 
{
	private String hadith;
	private String book;
	private String num_hadith;
	private String matn;
	private String sanad;
	private String sanad_length;
	
	public String getHadith() {
		return hadith;
	}
	public void setHadith(String i) {
		this.hadith = i;
	}
	public String getBook() {
		return book;
	}
	public void setBook(String book) {
		this.book = book;
	}
	public String getNum_hadith() {
		return num_hadith;
	}
	public void setNum_hadith(String num_hadith) {
		this.num_hadith = num_hadith;
	}
	public String getMatn() {
		return matn;
	}
	public void setMatn(String matn) {
		this.matn = matn;
	}
	public String getSanad() {
		return sanad;
	}
	public void setSanad(String sanad) {
		this.sanad = sanad;
	}
	public String getSanad_length() {
		return sanad_length;
	}
	public void setSanad_length(String sanad_length) {
		this.sanad_length = sanad_length;
	}
}
